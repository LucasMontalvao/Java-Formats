package br.com.LucasMontalvãoBongalhardo.horarioClasses;

public class Horario {
	protected int hora = 9;
	protected int minuto = 30;
	
	public int getHora() {
		return hora;
	}
	public void setHora(int hora) {
		this.hora = hora;
	}
	public int getMinuto() {
		return minuto;
	}
	public void setMinuto(int minuto) {
		this.minuto = minuto;
	}
	
}
